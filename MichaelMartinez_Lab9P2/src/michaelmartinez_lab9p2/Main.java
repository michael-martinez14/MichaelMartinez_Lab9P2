    private void jb_subirArchivoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_subirArchivoMouseClicked
        // TODO add your handling code here:
        JFileChooser jfc=new JFileChooser();
        FileNameExtensionFilter filtro=new FileNameExtensionFilter("Archivos de texto", "txt");
        jfc.setFileFilter(filtro);
        int seleccion=jfc.showSaveDialog(null);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            String nombre = jfc.getSelectedFile().getName();
            if (nombre.contains(".txt")) {
                File file = jfc.getSelectedFile();
                archivo=file;
                hiloBarra hiloBarra = new hiloBarra(pb_barraProgreso, ta_TextArea,file);
                Thread proceso2 = new Thread(hiloBarra);
                proceso2.start();
            }
        }
        

    }//GEN-LAST:event_jb_subirArchivoMouseClicked

    private void jb_guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_guardarMouseClicked
        FileWriter fw=null;
        try {
            // TODO add your handling code here:
            fw = new FileWriter(archivo);
            System.out.println(archivo.getPath());
            BufferedWriter bw=new BufferedWriter(fw);
            bw.write(ta_TextArea.getText());
            ta_TextArea.setText("");
            bw.flush();
            fw.close();
            bw.close();
            JOptionPane.showMessageDialog(this, "Archivo modificado");
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        
        
                
    }//GEN-LAST:event_jb_guardarMouseClicked

